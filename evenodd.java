import java.util.*;

import java.lang.*;

import java.io.*;

class Ideone
{

public static void main (String[] args) throws java.lang.Exception
	
{
	
Scanner sc=new Scanner(System.in);
	
int T=sc.nextInt();
	
if(T%2==0 && T>=0)
	
{
		
System.out.println("Even");
	
}
	
else if(T%2!=0 && T>=0)
	
{
		
System.out.println("Odd");
	
}
	
else
	
{
		
System.out.println("invalid");
	
}
	
}
}
